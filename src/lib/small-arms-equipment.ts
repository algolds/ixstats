// Filter by era
export function getSmallArmsByEra(era: keyof typeof WEAPON_ERAS) {
  const result: Record<string, any[]> = {};

  Object.entries(SMALL_ARMS_DATABASE).forEach(([category, items]) => {
    const filtered = Object.entries(items)
      .filter(([_, item]: [string, any]) => item.era === era)
      .map(([key, item]) => ({ ...item, key }));

    if (filtered.length > 0) {
      result[category] = filtered;
    }
  });

  return result;
}

// Filter by manufacturer
export function getSmallArmsByManufacturer(manufacturer: keyof typeof SMALL_ARMS_MANUFACTURERS) {
  const result: Record<string, any[]> = {};

  Object.entries(SMALL_ARMS_DATABASE).forEach(([category, items]) => {
    const filtered = Object.entries(items)
      .filter(([_, item]: [string, any]) => item.manufacturer === manufacturer)
      .map(([key, item]) => ({ ...item, key }));

    if (filtered.length > 0) {
      result[category] = filtered;
    }
  });

  return result;
}

